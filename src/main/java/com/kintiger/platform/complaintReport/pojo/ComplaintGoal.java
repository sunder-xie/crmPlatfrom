package com.kintiger.platform.complaintReport.pojo;

public class ComplaintGoal {
	private String startMonth;
	private String endMonth;
	private int gMonth;
	private String gFactory;
	private String gWorkShop;
	private double gGoal;
	
	public String getStartMonth() {
		return startMonth;
	}
	public void setStartMonth(String startMonth) {
		this.startMonth = startMonth;
	}
	public String getEndMonth() {
		return endMonth;
	}
	public void setEndMonth(String endMonth) {
		this.endMonth = endMonth;
	}
	public String getgFactory() {
		return gFactory;
	}
	public void setgFactory(String gFactory) {
		this.gFactory = gFactory;
	}
	public String getgWorkShop() {
		return gWorkShop;
	}
	public void setgWorkShop(String gWorkShop) {
		this.gWorkShop = gWorkShop;
	}
	public double getgGoal() {
		return gGoal;
	}
	public void setgGoal(double gGoal) {
		this.gGoal = gGoal;
	}
	public int getgMonth() {
		return gMonth;
	}
	public void setgMonth(int gMonth) {
		this.gMonth = gMonth;
	}
	

}
