function search() {
	var queryParams = $('#datagrid').datagrid('options').queryParams;
	queryParams.kunnrId = $('#kunnrId').val();
	queryParams.name1 = encodeURIComponent($('#kunnrName').val());
	queryParams.kunagId = $('#kunagId').val();
	queryParams.name2 = encodeURIComponent($('#kunagName').val());
	queryParams.orgId = $('#orgId').val();
	queryParams.isImportant = $('#isImportant').combobox('getValue');
	$("#datagrid").datagrid('load');
}
